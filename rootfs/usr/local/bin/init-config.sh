#!/bin/sh
# =============================================================================
# init-config.sh — Container initialization (runs as s6 oneshot)
#
# 1. Validates required environment variables
# 2. Sets up SSH key (generates if missing)
# 3. Configures SSH known_hosts handling
# 4. Clones the git remote into /vault (retries until success)
# 5. Configures git author
# 6. Sets up Git LFS (if enabled)
# 7. Writes vault .gitignore
# 8. Sets up obsidian-headless sync
#
# This script runs as root but delegates user-facing operations
# (git, ssh, ob) to the 'obsidian' user via su-exec. The init-usermap
# oneshot has already remapped the obsidian user's UID/GID to match
# PUID/PGID before this script runs.
# =============================================================================
set -eu

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------
LOG_TAG="init-config"
. /usr/local/lib/log-functions.sh

# Run a command as the obsidian user
run_as_user() {
  su-exec obsidian "$@"
}

# ---------------------------------------------------------------------------
# 1. Validate required environment variables
# ---------------------------------------------------------------------------
log "Validating environment variables..."

missing=""

# NOTE: OBSIDIAN_AUTH_TOKEN is named by obsidian-headless, not this project.
# It doesn't use the OBSIDIAN_GIT_ prefix because ob reads it directly.
[ -z "${OBSIDIAN_AUTH_TOKEN:-}" ]     && missing="${missing} OBSIDIAN_AUTH_TOKEN"
[ -z "${OBSIDIAN_GIT_VAULT_NAME:-}" ] && missing="${missing} OBSIDIAN_GIT_VAULT_NAME"
[ -z "${OBSIDIAN_GIT_REMOTE_URL:-}" ] && missing="${missing} OBSIDIAN_GIT_REMOTE_URL"

if [ -n "$missing" ]; then
  log_error "Missing required environment variables:"
  for var in ${missing}; do
    log_error "  - ${var}"
  done
  log_error "Copy .env.example to .env and fill in the required values."
  log_error "See the README for how to get each value."
  exit 1
fi

# Apply defaults for optional variables
OBSIDIAN_GIT_USER_NAME="${OBSIDIAN_GIT_USER_NAME:-obsidian-git-backup}"
OBSIDIAN_GIT_USER_EMAIL="${OBSIDIAN_GIT_USER_EMAIL:-obsidian-git-backup@local}"
OBSIDIAN_GIT_BRANCH="${OBSIDIAN_GIT_BRANCH:-main}"

log "Environment OK"
log "  Vault:  ${OBSIDIAN_GIT_VAULT_NAME}"
log "  Remote: ${OBSIDIAN_GIT_REMOTE_URL}"
log "  Branch: ${OBSIDIAN_GIT_BRANCH}"

# ---------------------------------------------------------------------------
# 2. SSH key setup
#
# Keys live directly in /config/ (not /config/.ssh/) so that users can
# easily bind-mount a host directory without dealing with hidden dotfile
# subdirectories, which are awkward in Docker volume mounts.
# ---------------------------------------------------------------------------
SSH_KEY="/config/id_ed25519"

if [ -f "${SSH_KEY}" ]; then
  log "Found existing SSH key at ${SSH_KEY}"
else
  log "No SSH key found. Generating a new ed25519 keypair..."
  run_as_user ssh-keygen -t ed25519 -f "${SSH_KEY}" -N "" -C "obsidian-git-backup"

  log_banner "NEW SSH KEY GENERATED"
  log "Add this public key to your git server (e.g., GitHub, GitLab):"
  log ""
  log "$(cat "${SSH_KEY}.pub")"
  log ""
  log "Key location: ${SSH_KEY} (inside the /config volume)"
  log ""
  log "The container will retry connecting every 30 seconds"
  log "until the key is authorized."
  log_banner "END SSH KEY"

  # Also log with prefix so it's filterable
  log "SSH public key: $(cat "${SSH_KEY}.pub")"
fi

# Fix permissions and ownership regardless of how the key got here.
# If the user bind-mounted a key, host permissions may not match what
# SSH requires (600 on private key). If we just generated it, this is
# a harmless no-op.
# NOTE: On macOS, chown on bind-mounted files may fail if PUID doesn't
# match the host file owner (see ADR-0002). We warn instead of aborting.
chmod 600 "${SSH_KEY}"
[ -f "${SSH_KEY}.pub" ] && chmod 644 "${SSH_KEY}.pub"
chown obsidian:obsidian "${SSH_KEY}" 2>/dev/null || \
  log "Warning: could not chown ${SSH_KEY}. On macOS, ensure PUID matches the file owner."
[ -f "${SSH_KEY}.pub" ] && { chown obsidian:obsidian "${SSH_KEY}.pub" 2>/dev/null || true; }

# ---------------------------------------------------------------------------
# 3. Configure SSH
#
# If the user provides their own ssh_config (e.g., for a jump host or
# non-standard port), we respect it. Otherwise, generate a default one.
# ---------------------------------------------------------------------------
SSH_CONFIG="/config/ssh_config"

if [ -f "${SSH_CONFIG}" ]; then
  log "Using existing SSH config at ${SSH_CONFIG}"
else
  log "Creating default SSH config..."
  (umask 077 && cat > "${SSH_CONFIG}" <<'EOF'
# Auto-generated by obsidian-git-backup init-config
# Accept host keys on first connection, reject changes after (TOFU)
Host *
    StrictHostKeyChecking accept-new
    UserKnownHostsFile /config/known_hosts
    IdentityFile /config/id_ed25519
EOF
  )
  chown obsidian:obsidian "${SSH_CONFIG}" 2>/dev/null || true
fi

# Pre-create known_hosts with correct ownership so SSH doesn't create
# it with whatever umask happens to be active at connection time.
if [ ! -f "/config/known_hosts" ]; then
  touch "/config/known_hosts"
  chmod 644 "/config/known_hosts"
  chown obsidian:obsidian "/config/known_hosts" 2>/dev/null || true
fi

# GIT_SSH_COMMAND is used by git commands within this script (e.g., clone).
# For the longrun services, core.sshCommand is set per-repo below (step 5).
export GIT_SSH_COMMAND="ssh -F ${SSH_CONFIG}"

log "SSH configured (StrictHostKeyChecking=accept-new)"

# ---------------------------------------------------------------------------
# 4. Git clone (with retry loop)
# ---------------------------------------------------------------------------
RETRY_INTERVAL=30

clean_vault() {
  # Remove all files including dotfiles, except /vault itself
  rm -rf /vault/.git
  # Using find to catch dotfiles that glob misses
  find /vault -mindepth 1 -maxdepth 1 -exec rm -rf {} + 2>/dev/null || true
}

if [ -d "/vault/.git" ]; then
  log "Git repository already exists at /vault"
  # Ensure remote is up to date
  run_as_user git -C /vault remote set-url origin "${OBSIDIAN_GIT_REMOTE_URL}" 2>/dev/null || true
else
  log "Cloning ${OBSIDIAN_GIT_REMOTE_URL} into /vault..."

  # Ensure /vault is writable by the obsidian user before cloning
  chown obsidian:obsidian /vault

  while true; do
    # Try with the configured branch first
    if run_as_user env GIT_SSH_COMMAND="${GIT_SSH_COMMAND}" git clone --branch "${OBSIDIAN_GIT_BRANCH}" "${OBSIDIAN_GIT_REMOTE_URL}" /vault 2>&1; then
      log "Git clone successful (branch: ${OBSIDIAN_GIT_BRANCH})"
      break
    fi

    # Clean up partial state before second attempt
    clean_vault

    # Branch might not exist yet on a new/empty repo — try default branch
    if run_as_user env GIT_SSH_COMMAND="${GIT_SSH_COMMAND}" git clone "${OBSIDIAN_GIT_REMOTE_URL}" /vault 2>&1; then
      log "Git clone successful (default branch). Will create '${OBSIDIAN_GIT_BRANCH}' branch."
      break
    fi

    # Both attempts failed — clean up and retry after delay
    clean_vault
    log "Git clone failed. Retrying in ${RETRY_INTERVAL}s..."
    log "If you just generated a new SSH key, add it to your git server."
    sleep "${RETRY_INTERVAL}"
  done
fi

# ---------------------------------------------------------------------------
# 5. Git configuration
# ---------------------------------------------------------------------------
run_as_user git -C /vault config user.name "${OBSIDIAN_GIT_USER_NAME}"
run_as_user git -C /vault config user.email "${OBSIDIAN_GIT_USER_EMAIL}"

# Set the SSH command persistently for this repo (used by longrun services)
run_as_user git -C /vault config core.sshCommand "ssh -F ${SSH_CONFIG}"

# Ensure we're on the right branch
CURRENT_BRANCH="$(run_as_user git -C /vault branch --show-current 2>/dev/null || echo "")"
if [ "${CURRENT_BRANCH}" != "${OBSIDIAN_GIT_BRANCH}" ]; then
  run_as_user git -C /vault checkout -B "${OBSIDIAN_GIT_BRANCH}" 2>/dev/null || true
fi

log "Git configured (user: ${OBSIDIAN_GIT_USER_NAME}, branch: ${OBSIDIAN_GIT_BRANCH})"

# ---------------------------------------------------------------------------
# 6. Git LFS setup (optional)
#
# When OBSIDIAN_GIT_LFS_ENABLED=true, initializes Git LFS in the repo
# and writes a managed .gitattributes block that tracks binary files by
# extension. Users can override the default extension list via
# OBSIDIAN_GIT_LFS_EXTENSIONS (comma-separated, e.g. "png,jpg,pdf").
#
# The .gitattributes file uses the same managed-block pattern as
# .gitignore (step 7) to allow updates on image upgrades while
# preserving user-added entries.
# ---------------------------------------------------------------------------
OBSIDIAN_GIT_LFS_ENABLED="${OBSIDIAN_GIT_LFS_ENABLED:-true}"

if [ "${OBSIDIAN_GIT_LFS_ENABLED}" = "true" ]; then
  log "Git LFS enabled — initializing..."

  # Install LFS hooks into the repo
  run_as_user git -C /vault lfs install --local 2>&1 | while IFS= read -r line; do
    log "lfs: ${line}"
  done

  # Build the .gitattributes managed block from the extensions list
  OBSIDIAN_GIT_LFS_EXTENSIONS="${OBSIDIAN_GIT_LFS_EXTENSIONS:-png,jpg,jpeg,gif,bmp,svg,webp,ico,tif,tiff,mp4,mov,avi,mkv,webm,mp3,wav,ogg,flac,m4a,aac,pdf,zip,tar,gz,7z,rar,doc,docx,xls,xlsx,ppt,pptx}"
  GITATTRIBUTES="/vault/.gitattributes"
  LFS_MARKER_START="# --- obsidian-git-backup LFS managed entries ---"
  LFS_MARKER_END="# --- end obsidian-git-backup LFS ---"

  # Build the LFS tracking lines from the comma-separated extension list
  lfs_lines=""
  # Use a temp var to iterate — avoids subshell issues with IFS
  _remaining="${OBSIDIAN_GIT_LFS_EXTENSIONS}"
  while [ -n "${_remaining}" ]; do
    # Extract the first extension (before the first comma)
    _ext="${_remaining%%,*}"
    # Remove leading/trailing whitespace from the extension
    _ext="$(echo "${_ext}" | tr -d ' ')"
    if [ -n "${_ext}" ]; then
      # Remove leading dot if user included one (e.g., ".png" → "png")
      _ext="${_ext#.}"
      lfs_lines="${lfs_lines}*.${_ext} filter=lfs diff=lfs merge=lfs -text
"
    fi
    # Remove the processed extension from the remaining list
    if [ "${_remaining}" = "${_remaining#*,}" ]; then
      # No more commas — we've processed the last item
      break
    fi
    _remaining="${_remaining#*,}"
  done

  LFS_MANAGED_BLOCK="${LFS_MARKER_START}
# Binary files tracked by Git LFS.
# Override with OBSIDIAN_GIT_LFS_EXTENSIONS (comma-separated extensions).
${lfs_lines}${LFS_MARKER_END}"

  if [ ! -f "${GITATTRIBUTES}" ]; then
    log "Creating vault .gitattributes with LFS tracking rules..."
    printf '%s\n' "${LFS_MANAGED_BLOCK}" > "${GITATTRIBUTES}"
  elif grep -q "${LFS_MARKER_START}" "${GITATTRIBUTES}" 2>/dev/null; then
    # Replace existing managed block (supports image upgrades / extension changes)
    log "Updating vault .gitattributes LFS managed entries..."
    tmp_attr="$(mktemp "${GITATTRIBUTES}.XXXXXX")"
    tmp_attr2="$(mktemp "${GITATTRIBUTES}.XXXXXX")"
    trap 'rm -f "${tmp_attr}" "${tmp_attr2}"' EXIT
    # Extract user content (everything outside the markers)
    in_lfs_block=false
    while IFS= read -r line || [ -n "${line}" ]; do
      case "${line}" in
        "${LFS_MARKER_START}")
          in_lfs_block=true
          ;;
        "${LFS_MARKER_END}")
          in_lfs_block=false
          ;;
        *)
          if ! ${in_lfs_block}; then
            printf '%s\n' "${line}"
          fi
          ;;
      esac
    done < "${GITATTRIBUTES}" > "${tmp_attr}"
    # Strip trailing blank lines (same technique as .gitignore)
    awk '
      { lines[NR] = $0 }
      END {
        last = NR
        while (last > 0 && lines[last] ~ /^[[:space:]]*$/) last--
        for (i = 1; i <= last; i++) print lines[i]
      }
    ' "${tmp_attr}" > "${tmp_attr2}"
    mv "${tmp_attr2}" "${tmp_attr}"
    if [ -s "${tmp_attr}" ]; then
      printf '\n%s\n' "${LFS_MANAGED_BLOCK}" >> "${tmp_attr}"
    else
      printf '%s\n' "${LFS_MANAGED_BLOCK}" > "${tmp_attr}"
    fi
    mv "${tmp_attr}" "${GITATTRIBUTES}"
    trap - EXIT
  else
    log "Appending LFS tracking rules to vault .gitattributes..."
    printf '\n%s\n' "${LFS_MANAGED_BLOCK}" >> "${GITATTRIBUTES}"
  fi

  chown obsidian:obsidian "${GITATTRIBUTES}"

  # Count tracked extensions for log output
  ext_count=0
  _remaining="${OBSIDIAN_GIT_LFS_EXTENSIONS}"
  while [ -n "${_remaining}" ]; do
    _ext="${_remaining%%,*}"
    _ext="$(echo "${_ext}" | tr -d ' ')"
    [ -n "${_ext}" ] && ext_count=$((ext_count + 1))
    if [ "${_remaining}" = "${_remaining#*,}" ]; then break; fi
    _remaining="${_remaining#*,}"
  done

  # Fetch LFS objects so the working tree contains real file content,
  # not LFS pointer stubs. This is critical for disaster recovery:
  # after a fresh git clone, LFS-tracked files are pointer files until
  # 'git lfs pull' fetches the actual content. If obsidian-headless
  # starts syncing before this, it would push pointer stubs to Obsidian
  # Sync, corrupting binary files on all other devices.
  log "Fetching LFS objects..."
  if lfs_pull_output="$(run_as_user git -C /vault lfs pull 2>&1)"; then
    log "LFS objects fetched"
  else
    log "Warning: git lfs pull failed (LFS objects may not be available yet)"
    log "  ${lfs_pull_output}"
    log "  This is expected on a new repo with no LFS objects."
  fi

  log "Git LFS configured (${ext_count} extensions tracked)"
  log "Your git remote must support LFS. If pushes fail, set OBSIDIAN_GIT_LFS_ENABLED=false"
else
  log "Git LFS disabled (OBSIDIAN_GIT_LFS_ENABLED=false)"
fi

# ---------------------------------------------------------------------------
# 7. Vault .gitignore
#
# Uses start/end markers so the block can be updated on image upgrades.
# ---------------------------------------------------------------------------
GITIGNORE="/vault/.gitignore"
MARKER_START="# --- obsidian-git-backup managed entries ---"
MARKER_END="# --- end obsidian-git-backup ---"

MANAGED_BLOCK="${MARKER_START}
# Obsidian workspace state (changes on every app focus, causes churn)
.obsidian/workspace*.json

# Obsidian graph view state (changes when graph is opened/panned)
.obsidian/graph.json

# Obsidian cache (rebuilt automatically, not user content)
.obsidian/cache/

# Obsidian trash (soft-delete holding area)
.trash/

# Uncomment the next line to ignore ALL plugin data.json files.
# WARNING: this also ignores plugin *settings* (Templater, Tasks,
# Periodic Notes, QuickAdd, etc.) — your backup will be incomplete.
# Only enable this if plugin-settings churn is unacceptable.
# .obsidian/plugins/*/data.json

# Uncomment the following lines if you do NOT want to track Obsidian
# app configuration (these change when you adjust settings on any device):
# .obsidian/app.json
# .obsidian/appearance.json
# .obsidian/community-plugins.json
# .obsidian/core-plugins.json
# .obsidian/core-plugins-migration.json
# .obsidian/hotkeys.json
${MARKER_END}"

if [ ! -f "${GITIGNORE}" ]; then
  log "Creating vault .gitignore..."
  printf '%s\n' "${MANAGED_BLOCK}" > "${GITIGNORE}"
elif grep -q "${MARKER_START}" "${GITIGNORE}" 2>/dev/null; then
  # Replace existing managed block (supports image upgrades)
  log "Updating vault .gitignore managed entries..."
  # Create temp files in the same directory as the target for atomic
  # rename(2) — /tmp and /vault may be different filesystems.
  tmp="$(mktemp "${GITIGNORE}.XXXXXX")"
  tmp2="$(mktemp "${GITIGNORE}.XXXXXX")"
  trap 'rm -f "${tmp}" "${tmp2}"' EXIT
  # Extract user content (everything outside the markers).
  # The || [ -n "${line}" ] handles files without a trailing newline —
  # without it, read returns non-zero at EOF and set -eu kills the script.
  in_block=false
  while IFS= read -r line || [ -n "${line}" ]; do
    case "${line}" in
      "${MARKER_START}")
        in_block=true
        ;;
      "${MARKER_END}")
        in_block=false
        ;;
      *)
        if ! ${in_block}; then
          printf '%s\n' "${line}"
        fi
        ;;
    esac
  done < "${GITIGNORE}" > "${tmp}"
  # Strip trailing blank lines from user content to prevent blank-line
  # accumulation across container restarts. Without this, each restart
  # adds one blank line: the \n separator from the previous run is kept
  # as "user content", then a new \n separator is added.
  awk '
    { lines[NR] = $0 }
    END {
      last = NR
      while (last > 0 && lines[last] ~ /^[[:space:]]*$/) last--
      for (i = 1; i <= last; i++) print lines[i]
    }
  ' "${tmp}" > "${tmp2}"
  mv "${tmp2}" "${tmp}"
  # Invariant: the file on disk will be exactly:
  #   {user content, trailing blank lines stripped}
  #   {blank line separator}   <- only if user content is non-empty
  #   {managed block}
  # This ensures the block can be re-processed on every restart
  # without accumulating blank lines.
  if [ -s "${tmp}" ]; then
    printf '\n%s\n' "${MANAGED_BLOCK}" >> "${tmp}"
  else
    printf '%s\n' "${MANAGED_BLOCK}" > "${tmp}"
  fi
  mv "${tmp}" "${GITIGNORE}"
  trap - EXIT
else
  log "Appending managed entries to vault .gitignore..."
  printf '\n%s\n' "${MANAGED_BLOCK}" >> "${GITIGNORE}"
fi

# Ensure .gitignore is owned by the obsidian user
chown obsidian:obsidian "${GITIGNORE}"

# ---------------------------------------------------------------------------
# 8. Obsidian headless setup
# ---------------------------------------------------------------------------
log "Setting up obsidian-headless sync..."

# Build args list properly to handle values with spaces and special chars
set -- --vault "${OBSIDIAN_GIT_VAULT_NAME}" --path /vault

if [ -n "${OBSIDIAN_GIT_E2EE_PASSWORD:-}" ]; then
  # NOTE: The password is passed as a CLI argument which is visible in
  # /proc/*/cmdline. obsidian-headless does not currently support
  # --password-file or --password-stdin. This is acceptable in a container
  # with its own PID namespace (the default).
  set -- "$@" --password "${OBSIDIAN_GIT_E2EE_PASSWORD}"
  log "E2EE password provided (${#OBSIDIAN_GIT_E2EE_PASSWORD} chars)"
else
  log "No E2EE password set (OBSIDIAN_GIT_E2EE_PASSWORD is empty)"
fi

# ob sync-setup is idempotent — safe to run even if already configured.
# OBSIDIAN_AUTH_TOKEN is read from the environment by ob directly.
# Run as the obsidian user so config files are owned correctly.
#
# Pipe an empty string to stdin so ob doesn't block waiting for an
# interactive password prompt if the --password argument is missing or
# incorrect. Without this, ob's fe() prompt reader hangs on non-TTY stdin.
ob_output="$(run_as_user env OBSIDIAN_AUTH_TOKEN="${OBSIDIAN_AUTH_TOKEN}" \
  ob sync-setup "$@" < /dev/null 2>&1)" \
  || ob_exit=$?

if [ "${ob_exit:-0}" -ne 0 ]; then
  log_error "obsidian-headless sync-setup failed (exit code: ${ob_exit:-?})"
  # Show the actual output from ob — it contains the real error
  if [ -n "${ob_output:-}" ]; then
    log_error "ob output: ${ob_output}"
  fi
  # Exit code 2 = password validation failure in ob
  if [ "${ob_exit:-0}" -eq 2 ]; then
    log_error "Password validation failed."
    if [ -n "${OBSIDIAN_GIT_E2EE_PASSWORD:-}" ]; then
      log_error "OBSIDIAN_GIT_E2EE_PASSWORD is set (${#OBSIDIAN_GIT_E2EE_PASSWORD} chars) but was rejected."
      log_error "Check that the password matches exactly (including special characters)."
      log_error "If your password contains special characters ($ \\ \" ' ! #), make sure"
      log_error "it is correctly set in your .env file. In Docker Compose .env files:"
      log_error "  - Values are taken literally (no shell expansion)"
      log_error "  - Do NOT add quotes around the value"
      log_error "  - Example: OBSIDIAN_GIT_E2EE_PASSWORD=my\$ecr3t!pa\$\$"
    else
      log_error "Your vault uses end-to-end encryption but OBSIDIAN_GIT_E2EE_PASSWORD is not set."
      log_error "Add it to your .env file: OBSIDIAN_GIT_E2EE_PASSWORD=<your-password>"
    fi
  # Exit code 3 = vault not found
  elif [ "${ob_exit:-0}" -eq 3 ]; then
    log_error "Vault '${OBSIDIAN_GIT_VAULT_NAME}' not found."
    log_error "Run 'ob sync-list-remote' to see available vault names and IDs."
  else
    log_error "Common causes:"
    log_error "  - OBSIDIAN_AUTH_TOKEN is invalid or expired (re-run 'ob login')"
    log_error "  - Network connectivity issues"
  fi
  exit 1
fi

# Show the successful output from ob
if [ -n "${ob_output:-}" ]; then
  echo "${ob_output}" | while IFS= read -r line; do
    log "ob: ${line}"
  done
fi

log "Obsidian headless sync configured for vault: ${OBSIDIAN_GIT_VAULT_NAME}"
log "Initialization complete"
